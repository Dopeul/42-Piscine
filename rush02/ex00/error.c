#include "ft.h"

int	check_error(char *argv)
{
	int	i;

	i = 0;
	while (argv[i])
	{
		if (argv[i] == '-' || !(48 <= argv[i] && argv[i] <= 57))
		{
			ft_putstr("error\n");
			return (0);
		}
		i++;
	}
	return (1);
}
