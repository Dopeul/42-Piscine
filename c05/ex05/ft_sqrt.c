int	ft_sqrt(int nb)
{
	int	i;

	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	i = 0;
	while ((i * i) < nb)
	{
		i++;
		if (i == 46341)
			return (0);
		else if ((i * i) > nb)
			return (0);
	}
	return (i);
}
